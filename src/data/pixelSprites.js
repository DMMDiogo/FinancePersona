/**
 * Draw a pixel sprite onto a canvas context.
 * @param {CanvasRenderingContext2D} ctx
 * @param {number[][]} grid  - 2D array of color indices
 * @param {Object} palette   - { [index]: hexColor }
 * @param {number} cellSize  - px per grid cell
 * @param {number} offsetX   - left offset in canvas px
 * @param {number} offsetY   - top offset in canvas px
 */
export function drawSprite(ctx, grid, palette, cellSize, offsetX = 0, offsetY = 0) {
  ctx.imageSmoothingEnabled = false;
  for (let row = 0; row < grid.length; row++) {
    for (let col = 0; col < grid[row].length; col++) {
      const idx = grid[row][col];
      if (idx === 0) continue;
      const color = palette[idx];
      if (!color) continue;
      ctx.fillStyle = color;
      ctx.fillRect(
        offsetX + col * cellSize,
        offsetY + row * cellSize,
        cellSize,
        cellSize,
      );
    }
  }
}
